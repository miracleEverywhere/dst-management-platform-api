package utils

import "encoding/hex"

func GetSteamApiKey() string {
	obfuscated := []byte{
		0xD5, 0xED, 0xDA, 0x66, 0x64, 0xFF, 0x23, 0xA6,
		0xB3, 0xD8, 0x50, 0x2C, 0x63, 0xB1, 0xBF, 0x6D,
	}
	var data []byte
	for _, b := range obfuscated {
		data = append(data, b^0x55)
	}
	return hex.EncodeToString(data)
}
